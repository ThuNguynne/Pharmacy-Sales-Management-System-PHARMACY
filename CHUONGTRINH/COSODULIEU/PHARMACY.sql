-- ── 8.1 Gia hạn HĐ đã hết hạn (NGOẠI TRỪ các tài khoản được sinh ra để test Hết Hạn)
UPDATE KHACHHANGSI
SET    NGAYHETHAN      = DATEADD(YEAR, 2, GETDATE()),
       SOLANSIMATKHAU  = 0,                             
       TRANGTHAID      = N'HieuLuc'                 
WHERE  NGAYHETHAN < GETDATE()
  AND  TRANGTHAID  IN (N'HieuLuc', N'HetHan')
  -- BỎ QUA KH032, KH033 vì đây là dữ liệu CỐ TÌNH tạo hết hạn để test nghiệp vụ chặn đăng nhập
  AND  MAKH NOT IN ('KH032', 'KH033');      

-- ── 8.2 Riêng KH019 (TamNgung do công nợ): chỉ gia hạn HĐ, giữ TamNgung
UPDATE KHACHHANGSI
SET    NGAYHETHAN = DATEADD(YEAR, 2, GETDATE())
WHERE  MAKH = 'KH019'
  AND  TRANGTHAID = N'TamNgung';

-- ── 8.3 Kiểm tra kết quả tổng hợp
SELECT
    KS.MAKH,
    KH.HOTEN,
    KS.TENCONGTY,
    KS.TENDANGNHAP,
    KS.SOLANSIMATKHAU  AS SoLanSai,
    KS.TRANGTHAID,
    CONVERT(VARCHAR, KS.NGAYKYHD,   103) AS NgayKyHD,
    CONVERT(VARCHAR, KS.NGAYHETHAN, 103) AS NgayHetHan,
    CASE WHEN KS.NGAYHETHAN >= CAST(GETDATE() AS DATE) THEN N'✅ Còn hạn'
         ELSE N'❌ Hết hạn' END          AS TinhTrang
FROM  KHACHHANGSI KS
JOIN  KHACHHANG   KH ON KH.MAKH = KS.MAKH
ORDER BY KS.MAKH;

-- ── 8.4 Test đăng nhập thử với các Khách Hàng Sỉ mới thêm (Nghiệp vụ test Hết Hạn)
PRINT N'--- DANH SÁCH TÀI KHOẢN TEST CHO KHÁCH SỈ MỚI ---'
SELECT
    KS.TENDANGNHAP,
    KS.MATKHAU,
    KS.TRANGTHAID,
    CONVERT(VARCHAR, KS.NGAYHETHAN, 103) AS NgayHetHan,
    CASE WHEN KS.TRANGTHAID = N'HieuLuc' AND KS.NGAYHETHAN >= CAST(GETDATE() AS DATE) 
         THEN N'🟢 Login OK' 
         ELSE N'🔴 Login FAIL' END AS KetQuaLogin
FROM KHACHHANGSI KS
WHERE KS.MAKH IN ('KH031', 'KH032', 'KH033', 'KH034');
