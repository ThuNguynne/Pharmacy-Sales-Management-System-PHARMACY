CREATE TRIGGER TR_TonKhoSauPhieuNhap ON PHIEUNHAP AFTER UPDATE AS
BEGIN
    SET NOCOUNT ON
    IF UPDATE(TRANGTHAI)
    BEGIN
        IF EXISTS (SELECT 1 FROM INSERTED WHERE TRANGTHAI = N'HoanThanh')
        BEGIN
            UPDATE lh SET lh.SOLUONGTON = lh.SOLUONGTON + ctn.SOLUONGNHAP
            FROM LOHANG lh JOIN CHITIETNHAP ctn ON ctn.MATHUOC = lh.MATHUOC AND ctn.SOLO = lh.SOLO
            JOIN INSERTED i ON i.MAPHIEUNHAP = ctn.MAPHIEUNHAP AND i.TRANGTHAI = N'HoanThanh' WHERE lh.MACN = i.MACN

            INSERT INTO LOHANG (MALO, MATHUOC, MACN, SOLO, NGAYSX, HANSD, SOLUONGTON, GIANHAP)
            SELECT 'LO-' + LEFT(REPLACE(NEWID(), '-', ''), 12), ctn.MATHUOC, i.MACN, ctn.SOLO, ctn.NGAYSX, ctn.HANSD, ctn.SOLUONGNHAP, ctn.DONGIANHAP
            FROM CHITIETNHAP ctn JOIN INSERTED i ON i.MAPHIEUNHAP = ctn.MAPHIEUNHAP AND i.TRANGTHAI = N'HoanThanh'
            WHERE NOT EXISTS (SELECT 1 FROM LOHANG lh2 WHERE lh2.MATHUOC = ctn.MATHUOC AND lh2.SOLO = ctn.SOLO AND lh2.MACN = i.MACN)
        END
    END
END
