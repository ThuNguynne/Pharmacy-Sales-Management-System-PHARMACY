-- ── CHINHANH ──────────────────────────────────────────────────────
CREATE TABLE CHINHANH (
    MACN        VARCHAR(10)   CONSTRAINT PK_CHINHANH PRIMARY KEY,
    TENCN       NVARCHAR(100) NOT NULL,
    DIACHI      NVARCHAR(200) NOT NULL,
    SDT         VARCHAR(15)   NOT NULL  CONSTRAINT UQ_CN_SDT   UNIQUE,
    EMAIL       VARCHAR(100)  NULL      CONSTRAINT CHK_CN_EMAIL CHECK (EMAIL LIKE '%@%' OR EMAIL IS NULL),
    MASOTHUE    VARCHAR(20)   NULL,
    TRANGTHAI   NVARCHAR(20)  NOT NULL  DEFAULT N'HoatDong'
                CONSTRAINT CHK_CN_TT CHECK (TRANGTHAI IN (N'HoatDong', N'TamNgung'))
)
GO

